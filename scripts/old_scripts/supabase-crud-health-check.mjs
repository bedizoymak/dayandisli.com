import { createClient } from "@supabase/supabase-js";
import { existsSync, readFileSync } from "node:fs";
import { randomUUID } from "node:crypto";

const CREATE_TABLE_SQL = `create table if not exists public.smoke_test (
  id bigint generated by default as identity primary key,
  name text not null,
  created_at timestamptz default now()
);

grant usage on sequence public.smoke_test_id_seq to authenticated;
grant select, insert, update, delete on table public.smoke_test to authenticated;

alter table public.smoke_test enable row level security;

create policy "authenticated smoke test access"
on public.smoke_test
for all
to authenticated
using (name like 'crud-health-%')
with check (name like 'crud-health-%');`;

function loadLocalEnvironment() {
  const processEnvironmentKeys = new Set(Object.keys(process.env));

  for (const file of [".env", ".env.local"]) {
    if (!existsSync(file)) continue;

    for (const line of readFileSync(file, "utf8").split(/\r?\n/)) {
      const match = line.match(/^\s*([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(.*)\s*$/);
      if (!match || processEnvironmentKeys.has(match[1])) continue;

      let value = match[2];
      if (
        (value.startsWith('"') && value.endsWith('"')) ||
        (value.startsWith("'") && value.endsWith("'"))
      ) {
        value = value.slice(1, -1);
      }
      process.env[match[1]] = value;
    }
  }
}

function fail(message, details) {
  console.error(`Supabase CRUD Health Check failed: ${message}`);
  if (details) console.error(`- detail: ${details}`);
  process.exitCode = 1;
}

function safeError(error) {
  if (!error || typeof error !== "object") return "Unknown Supabase error";
  const candidate = error;
  return [candidate.code, candidate.message, candidate.hint].filter(Boolean).join(" | ");
}

function printCreateInstructions() {
  console.error("The dedicated public.smoke_test table is unavailable.");
  console.error("Review and apply this SQL through the linked Supabase CLI or SQL Editor:");
  console.error(CREATE_TABLE_SQL);
}

async function run() {
  console.log("Supabase CRUD Health Check");

  if (process.env.RUN_SUPABASE_CRUD_HEALTH_CHECK !== "1") {
    fail("refusing to run without RUN_SUPABASE_CRUD_HEALTH_CHECK=1");
    return;
  }

  loadLocalEnvironment();

  const url = process.env.VITE_SUPABASE_URL;
  const key = process.env.VITE_SUPABASE_PUBLISHABLE_KEY ?? process.env.VITE_SUPABASE_ANON_KEY;

  if (!url || !key) {
    fail("frontend-safe Supabase URL and publishable/anon key are required");
    return;
  }

  if (process.env.SUPABASE_SERVICE_ROLE_KEY || process.env.VITE_SUPABASE_SERVICE_ROLE_KEY) {
    fail("service-role credentials must not be present when this script runs");
    return;
  }

  if (process.env.ALLOW_CREATE_SMOKE_TEST_TABLE === "1") {
    printCreateInstructions();
    fail("automatic table creation is unavailable without an explicitly reviewed linked-CLI method");
    return;
  }

  const supabase = createClient(url, key, {
    auth: { persistSession: false, autoRefreshToken: false, detectSessionInUrl: false },
  });

  const email = process.env.SUPABASE_SMOKE_TEST_EMAIL;
  const password = process.env.SUPABASE_SMOKE_TEST_PASSWORD;
  if (Boolean(email) !== Boolean(password)) {
    fail("SUPABASE_SMOKE_TEST_EMAIL and SUPABASE_SMOKE_TEST_PASSWORD must be provided together");
    return;
  }

  if (email && password) {
    const { error } = await supabase.auth.signInWithPassword({ email, password });
    if (error) {
      fail("dedicated smoke-user authentication failed", safeError(error));
      return;
    }
    console.log("- auth: ok");
  } else {
    console.log("- auth: anonymous client");
  }

  const insertedName = `crud-health-${randomUUID()}`;
  const updatedName = `${insertedName}-updated`;
  let rowId;

  try {
    const { data: inserted, error: insertError } = await supabase
      .from("smoke_test")
      .insert({ name: insertedName })
      .select("id, name")
      .single();

    if (insertError) {
      const detail = safeError(insertError);
      if (insertError.code === "42P01" || insertError.code === "PGRST205") printCreateInstructions();
      fail("insert failed", detail);
      return;
    }
    rowId = inserted.id;
    console.log("- connect: ok");
    console.log("- insert: ok");

    const { data: selected, error: selectError } = await supabase
      .from("smoke_test")
      .select("id, name")
      .eq("id", rowId)
      .single();
    if (selectError || selected?.name !== insertedName) throw new Error(`select failed: ${safeError(selectError)}`);
    console.log("- select: ok");

    const { data: updated, error: updateError } = await supabase
      .from("smoke_test")
      .update({ name: updatedName })
      .eq("id", rowId)
      .select("id, name")
      .single();
    if (updateError || updated?.name !== updatedName) throw new Error(`update failed: ${safeError(updateError)}`);
    console.log("- update: ok");

    const { error: deleteError } = await supabase.from("smoke_test").delete().eq("id", rowId);
    if (deleteError) throw new Error(`delete failed: ${safeError(deleteError)}`);
    console.log("- delete: ok");

    const { data: deleted, error: confirmError } = await supabase
      .from("smoke_test")
      .select("id")
      .eq("id", rowId)
      .maybeSingle();
    if (confirmError || deleted) throw new Error(`cleanup confirmation failed: ${safeError(confirmError)}`);
    rowId = undefined;
    console.log("- cleanup: ok");
  } catch (error) {
    fail(error instanceof Error ? error.message : "unexpected CRUD failure");
  } finally {
    if (rowId !== undefined) {
      const { error } = await supabase.from("smoke_test").delete().eq("id", rowId);
      console.log(error ? "- cleanup: failed" : "- cleanup: ok");
      if (error) process.exitCode = 1;
    }
    if (email && password) await supabase.auth.signOut();
  }
}

await run();
